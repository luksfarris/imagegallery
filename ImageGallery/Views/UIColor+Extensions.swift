import UIKit

extension UIColor {
    public static func niceGray() -> UIColor {
        return UIColor(red: 232 / 255, green: 232 / 255, blue: 232 / 255, alpha: 1)
    }

    public static func niceGreen() -> UIColor {
        return UIColor(red: 0 / 255, green: 87 / 255, blue: 49 / 255, alpha: 1)
    }

    public static func blackPhotoBackground() -> UIColor {
        return UIColor(red: 0 / 255, green: 0 / 255, blue: 0 / 255, alpha: 1)
    }
}
